CREATE PROCEDURE SP_Customer_Details
	@CustomerId  int
AS
BEGIN  
	SELECT * FROM 
		(
			VALUES	
				(@CustomerId, 500 + @CustomerId, '(' +  CONVERT(varchar(3), @CustomerId) + ')' + CONVERT(varchar(7), ( 1010101 + @CustomerId )), 'Details for customer ' + CONVERT(varchar(3), @CustomerId )),				
				(@CustomerId, 510 + @CustomerId, '(' +  CONVERT(varchar(3), @CustomerId) + ')' + CONVERT(varchar(7), ( 9741247 + @CustomerId )), 'Details for customer ' + CONVERT(varchar(3), @CustomerId )),				
				(@CustomerId, 597 + @CustomerId, '(' +  CONVERT(varchar(3), @CustomerId) + ')' + CONVERT(varchar(7), ( 1337337 + @CustomerId )), 'Details for customer ' + CONVERT(varchar(3), @CustomerId ))
		) 
		AS ResultSet(CustomerId, DetailId, PhoneNumber, Description);
		 
END
